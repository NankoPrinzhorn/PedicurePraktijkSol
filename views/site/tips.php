						<span class="tips-titel"><p>SCHOEISEL EN SOKKEN</p></span>
						<p class="padding_tekst1">Draag bij voorkeur katoenen of wollen sokken zonder dikke naden en verschoon ze dagelijks.
            Goed passende schoenen, die bovendien voldoende ventileren, zijn essentieel in uw dagelijks loopcomfort en in het voorkomen van pijnlijke voeten.
            Laat u voorlichten in een goede schoenenzaak over de voor u juiste maat en pasvorm. Koop uw schoenen bovendien aan het einde van de dag dan kunnen uw voeten namelijk al wat opgezet zijn.</p>
          	<p>Wissel zo mogelijk dagelijks van schoenen</p>
          	<p>Voorkom ingegroeide teennagels</p>
          	<p>De grootste risicofactoren voor het ontstaan van ingroeiende teennagels zijn:</p>
						<div class="tussenruimte"></div>
          	<p>•	Druk door slecht passend schoeisel</p>
          	<p>•	Het verkeerd (te kort en/of niet recht) knippen van de nagels</p>
          	<p>•	Een weke nagelomgeving door veel baden, transpiratie of synthetische sokken</p>
						<div class="tussenruimte"></div>
