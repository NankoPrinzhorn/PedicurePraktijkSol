					<span class="tips-titel"><p>VOETEN WASSEN</p></span>
					<p>Was uw voeten dagelijks met lauw water en bij voorkeur zonder zeep.
          Blijf ook niet te lang met uw voeten in het water, een droge huid kan hiervan het gevolg zijn.
